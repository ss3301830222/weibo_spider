    def start_requests(self):
        yield scrapy.Request(url=self.start_url.format(self.id, self.attitude_page), callback=self.parse_attitude,
                             headers=self.headers,cookies=self.settings['COOKIES'])#,meta={'proxy': 'https://{}'.format(self.proxy)}

    def parse_attitude(self,response):
        html = response.text
        html = json.loads(html)
        self.attitude_page += 1
        next_url = self.start_url.format(self.id, self.attitude_page)
        try:
            data = html['data']['data']
            total_number = html['data']['total_number']
            for i in range(len(data)):
                item = AttitudeItem()
                item['star_weibo_id'] = self.id
                item['total_number'] = total_number
                item['attitude_id'] = data[i]['id']#此条赞的ｉｄ
                item['created_at'] = data[i]['created_at']
                item['source'] = data[i]['source']
                user = data[i]['user']
                item['id'] = user['id']
                item['screen_name'] = user['screen_name']
                item['profile_image_url'] = user['profile_image_url']
                item['verified'] = user['verified']
                item['followers_count'] = user['followers_count']
                item['mbtype'] = user['mbtype']
                item['profile_url'] = user['profile_url']
                yield item
        except:
            print("点赞不存在")
        if html['ok'] == 1:
            headers = {
                'USER_AGENT': random.choice(self.user_agent)
            }
